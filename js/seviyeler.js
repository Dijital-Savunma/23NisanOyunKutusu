// Seviye verilerini zemin yüksekliğine göre normalleştir
function normalizeLevelData(levelData, groundY) {
    const normalized = JSON.parse(JSON.stringify(levelData));

    for (const plat of normalized.platforms) {
        plat.y = groundY + plat.y;
    }
    for (const coin of normalized.coins) {
        coin.y = groundY + coin.y;
    }
    for (const haz of normalized.hazards) {
        haz.y = groundY + haz.y;
    }
    for (const dec of normalized.decorations) {
        if (!dec.y) dec.y = groundY;
    }

    return normalized;
}
